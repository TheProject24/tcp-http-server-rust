use std::io::Write;
use std::net::TcpListener;
use std::thread;

use tcp_http_server_rust::request::Request;
use tcp_http_server_rust::response::{
    get_default_headers, write_headers, write_status_line, StatusCode,
};

/// Type alias defining the signature of a route handler.
/// 
/// Handlers receive a writer for outputting raw bytes back to the stream,
/// and a parsed `Request` representing the client's payload.
pub type Handler = fn(w: &mut dyn Write, req: &Request) -> Result<(), HandlerError>;

/// Handles incoming TCP connections and orchestrates the request routing.
pub struct Server {
    /// Background listener waiting for new TCP streams.
    listener: TcpListener,
    /// Function to process fully parsed requests.
    handler: Handler,
    /// Boolean flag to shut down the server.
    closed: bool,
}

/// Represents an HTTP error generated by the handler.
pub struct HandlerError {
    /// The HTTP status code (e.g., 400 or 500).
    pub status_code: StatusCode,
    /// A custom error message payload.
    pub message: String,
}

impl Server {
    /// Instantiates and binds a new server to the default localhost port 42069.
    pub fn serve(handler: Handler) -> Server {
        Server {
            listener: TcpListener::bind("127.0.0.1:42069").unwrap(),
            handler,
            closed: false,
        }
    }

    /// Spawns a dedicated thread to listen for and process incoming TCP connections.
    ///
    /// The spawned thread loops infinitely, blocking on `self.listener.incoming()`.
    /// Each accepted stream attempts to parse an HTTP `Request` which is passed
    /// alongside a buffered body `Vec<u8>` into the server's `handler`.
    pub fn listen(self) -> thread::JoinHandle<()> {
        thread::spawn(move || {
            let handler = self.handler;
            for incoming in self.listener.incoming() {
                if self.closed {
                    break;
                }

                let mut stream = match incoming {
                    Ok(s) => s,
                    Err(_) => {
                        eprintln!("test");
                        continue;
                    }
                };

                match Request::from_reader(&mut stream) {
                    Ok(req) => {
                        let mut body_buf: Vec<u8> = Vec::new();
                        let handler_res = handler(&mut body_buf, &req);
                        match handler_res {
                            Ok(()) => {
                                if let Err(e) = write_status_line(&mut stream, StatusCode::Ok) {
                                    eprintln!("write status err: {}", e);
                                    continue;
                                }

                                let headers = get_default_headers(body_buf.len());
                                if let Err(e) = write_headers(&mut stream, &headers) {
                                    eprintln!("write err: {}", e);
                                    continue;
                                }

                                if let Err(e) = stream.write_all(&body_buf) {
                                    eprintln!("write body err: {}", e);
                                    continue;
                                }

                                if let Err(e) = stream.flush() {
                                    eprintln!("flush err: {}", e);
                                    continue;
                                }
                            }
                            Err(err) => {
                                if let Err(e) = write_status_line(&mut stream, err.status_code) {
                                    eprintln!("write status err: {}", e);
                                    continue;
                                }

                                let error_body = err.message.into_bytes();
                                let headers = get_default_headers(error_body.len());
                                if let Err(e) = write_headers(&mut stream, &headers) {
                                    eprintln!("write err: {}", e);
                                    continue;
                                }

                                if let Err(e) = stream.write_all(&error_body) {
                                    eprintln!("write body err: {}", e);
                                    continue;
                                }

                                if let Err(e) = stream.flush() {
                                    eprintln!("flush err: {}", e);
                                    continue;
                                }
                            }
                        }
                    }
                    Err(_) => {
                        if let Err(e) = write_status_line(&mut stream, StatusCode::BadRequest) {
                            eprintln!("write status err: {}", e);
                            continue;
                        }

                        let headers = get_default_headers(0);
                        if let Err(e) = write_headers(&mut stream, &headers) {
                            eprintln!("write err: {}", e);
                            continue;
                        }

                        if let Err(e) = stream.flush() {
                            eprintln!("flush err: {}", e);
                            continue;
                        }
                    }
                }
            }
        })
    }

    /// Allows gracefully terminating the server thread processing.
    /// Note: Currently a no-op placeholder.
    pub fn close(self) {}
}

/// Standalone entry point. Ignored during normal usage where `serve(...)` is called elsewhere.
fn main() {}
